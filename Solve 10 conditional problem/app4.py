#4> Fruit Ripeness Checker
#Determine if a fruit is ripe, overripe bassed on its color.(e.g..Banana:Green - Unripe, Yellow - Rip,Brown-Overripe)

fruit = "Banana"
color = "Yellow"

if fruit == "Banana":
    if color == "Green":
        print("Unripe")
    elif color == "Yellow":
        print("Ripe")
    elif color == "Brown":
        print("OverRipe")
