#Suggest an activity based on the weather(e.g..Sunny - Go for walk,RAINY-Read a book,Snowy-Build snowan)
weather = "Sunny"
if weather == "Sunny":
    activity  = "Go for a walk"
elif weather == "Rainy":
    activity = "Read a book"
elif weather == "Snowy":
    activity = "Build a snowman" 

print(activity)